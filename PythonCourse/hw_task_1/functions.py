# 6)	* Функция на вход получает список, состоящий из 5 произвольных чисел. Найти количество положительных чисел
# среди них.

def countPositive(list):
    count = 0
    for i in list:
        if i > 0:
            count += 1
    return count


print(countPositive([1, 2, 3.0, 4, 5, -9.5, -5, -4]))


