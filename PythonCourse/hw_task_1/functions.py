# 9)	* Напишите функцию для нахождения факториала числа. ●	Факториал числа — это произведение всех целых чисел от
# 1 до искомого числа. К примере, факториал числа 6 равен 1*2*3*4*5*6 = 720.

def factorial(n):
    if n < 1:
        return 'нельзя вычислить'
    if n == 1:
        return 1
    return n * factorial(n - 1)


print(factorial(10))

